// JavaScript Arithmetic Examples

// Addition
let sum = 10 + 5;
console.log("Addition: 10 + 5 =", sum);

// Subtraction
let difference = 10 - 5;
console.log("Subtraction: 10 - 5 =", difference);

// Multiplication
let product = 10 * 5;
console.log("Multiplication: 10 * 5 =", product);

// Division
let quotient = 10 / 5;
console.log("Division: 10 / 5 =", quotient);

// Modulus (Remainder)
let remainder = 10 % 3;
console.log("Modulus: 10 % 3 =", remainder);

// Exponentiation
let power = 2 ** 3;
console.log("Exponentiation: 2 ** 3 =", power);

// Increment
let increment = 5;
increment++;
console.log("Increment: 5++ =", increment);

// Decrement
let decrement = 5;
decrement--;
console.log("Decrement: 5-- =", decrement);