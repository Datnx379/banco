let date = new Date();

// Set the year to 2025
date.setFullYear(2025);
console.log("Updated Year:", date);

// Set the month to December (Months are 0-indexed, so 11 = December)
date.setMonth(11);
console.log("Updated Month:", date);

// Set the date to the 25th
date.setDate(25);
console.log("Updated Date:", date);

// Set the hours to 15 (3 PM)
date.setHours(15);
console.log("Updated Hours:", date);

// Set the minutes to 45
date.setMinutes(45);
console.log("Updated Minutes:", date);

// Set the seconds to 30
date.setSeconds(30);
console.log("Updated Seconds:", date);

// Set the milliseconds to 500
date.setMilliseconds(500);
console.log("Updated Milliseconds:", date);