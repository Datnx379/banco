// Example of JSON Array Literals

// A JSON array containing strings
const fruits = ["Apple", "Banana", "Cherry"];
console.log(fruits);

// A JSON array containing numbers
const numbers = [10, 20, 30, 40];
console.log(numbers);

// A JSON array containing objects
const employees = [
    { name: "John", age: 30, position: "Developer" },
    { name: "Jane", age: 25, position: "Designer" },
    { name: "Mike", age: 35, position: "Manager" }
];
console.log(employees);

// A JSON array containing mixed data types
const mixedArray = ["Hello", 42, true, { key: "value" }];
console.log(mixedArray);