// Example of JSON Syntax

// A JSON object
const jsonObject = {
    "name": "John Doe",
    "age": 30,
    "isStudent": false,
    "skills": ["JavaScript", "HTML", "CSS"],
    "address": {
        "street": "123 Main St",
        "city": "New York",
        "zip": "10001"
    }
};

// Accessing JSON data
console.log(jsonObject.name); // Output: John Doe
console.log(jsonObject.skills[0]); // Output: JavaScript
console.log(jsonObject.address.city); // Output: New York