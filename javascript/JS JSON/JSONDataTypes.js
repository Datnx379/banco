// JSON supports the following data types:
// 1. String
// 2. Number
// 3. Boolean
// 4. Array
// 5. Object
// 6. null

// Example of JSON data types
const jsonData = {
    "name": "John",          // String
    "age": 30,               // Number
    "isStudent": false,      // Boolean
    "hobbies": ["reading", "gaming", "coding"], // Array
    "address": {             // Object
        "city": "New York",
        "zip": "10001"
    },
    "graduationYear": null   // null
};

console.log(jsonData);